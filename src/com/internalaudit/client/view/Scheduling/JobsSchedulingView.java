package com.internalaudit.client.view.Scheduling;

import com.google.gwt.user.client.ui.Composite;
import com.google.gwt.user.client.ui.HorizontalPanel;
import com.google.gwt.user.client.ui.VerticalPanel;

public class JobsSchedulingView extends Composite {
	
	private HorizontalPanel headingsPanel = new HorizontalPanel();
	private VerticalPanel listContainer = new VerticalPanel();
	
	public JobsSchedulingView(){
		
		VerticalPanel vpnlMain = new VerticalPanel();
		initWidget(vpnlMain);
		vpnlMain.add(headingsPanel);
		vpnlMain.add(listContainer);
		
	}

	public HorizontalPanel getHeadingsPanel() {
		return headingsPanel;
	}

	public void setHeadingsPanel(HorizontalPanel headingsPanel) {
		this.headingsPanel = headingsPanel;
	}

	public VerticalPanel getListContainer() {
		return listContainer;
	}

	public void setListContainer(VerticalPanel listContainer) {
		this.listContainer = listContainer;
	}
	
	

}
