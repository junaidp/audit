package com.internalaudit.shared;

import java.io.Serializable;
import java.util.ArrayList;

public class JobCreationDTO implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private JobCreation job = new JobCreation();
	
	private ArrayList<JobEmployeeRelation> jobEmpRelation = new ArrayList<JobEmployeeRelation>();
	
	private ArrayList<JobSkillRelation> jobSkillRelation = new ArrayList<JobSkillRelation>();
	

	public JobCreation getJob() {
		return job;
	}

	public void setJob(JobCreation job) {
		this.job = job;
	}

	public ArrayList<JobEmployeeRelation> getRelation() {
		return jobEmpRelation;
	}

	public void setRelation(ArrayList<JobEmployeeRelation> relation) {
		this.jobEmpRelation = relation;
	}

	public ArrayList<JobSkillRelation> getJobSkillRelation() {
		return jobSkillRelation;
	}

	public void setJobSkillRelation(ArrayList<JobSkillRelation> skillrelations) {
		this.jobSkillRelation = skillrelations;
	}

}
